# Useful Methods:
# --------------------------------------------------------------------------------------------

# Calculate the Length of a String
# Returns the number of characters in the string
string_length = len("John")  # Result: 4

# Check the Type of Given Input
# Returns the type of the input
input_type = type("Word")  # Result: <class 'str'>

# Rounding Floats
# Rounds a float to the nearest whole number
rounded_whole = round(8 / 3)  # Result: 3

# Rounds a float to a specified number of decimal places
rounded_two_decimals = round(2.6666666, 2)  # Result: 2.67

# Type Conversion
# Convert an integer to a string
string_conversion = str(123)  # Result: "123"

# Convert a string to an integer
integer_conversion = int("123")  # Result: 123

# Convert a string to a float
float_conversion = float("123.5")  # Result: 123.5
